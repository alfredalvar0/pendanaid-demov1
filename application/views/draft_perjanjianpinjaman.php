<p>4.	Bahwa berdasarkan hal-hal tersebut di atas, kedua belah pihak menyatakan sepakat dan setuju untuk mengadakan Perjanjian Kerjasama ini yang dilaksanakan dengan ketentuan dan syarat-syarat sebagai berikut :
</p>




<p>PASAL I</p>
<p>MAKSUD DAN TUJUAN
</p>
<p>Pihak Pertama dalam perjanjian ini memberi DANA INVESTASI kepada Pihak Kedua sebesar Rp.……………………..,- ( terbilang ) dan Pihak Kedua dengan ini telah menerima penyerahan DANA INVESTASI tersebut dari Pihak Pertama serta menyanggupi untuk melaksanakan pengelolaan DANA INVESTASI tersebut.
</p>

<p>PASAL II</p>
<p>RUANG LINGKUP</p>

<p>1.	Dalam pelaksanaan perjanjian ini, Pihak Pertama memberi DANA INVESTASI kepada Pihak Kedua sebesar Rp. ………………………,- (terbilan) dan Pihak Kedua dengan ini telah menerima penyerahan DANA INVESTASI tersebut dari Pihak Pertama serta menyanggupi untuk  melaksanakan pengelolaan DANA INVESTASI.
</p>
<p>2.	Pihak Kedua dengan ini berjanji dan mengikatkan diri untuk melaksanakan perputaran DANA INVESTASI pada Usaha Peningkatan Modal Investasi di bidang …………………….yang berlokasi di …………………………………………… setelah ditandatanganinya perjanjian ini.
</p>
<p>3.	Pihak Kedua dengan ini berjanji dan mengikatkan diri untuk memberikan keuntungan 10% (sepuluh persen) atau sebesar Rp. ……………………….,- (terbilang) dalam jangka waktu 1.5bulan.
</p>

<p>PASAL III</p>
<p>JANGKA WAKTU KERJASAMA</p>

<p>1.	Perjanjian kerjasama ini dilakukan dan diterima untuk jangka waktu 1,5 (satu setengah) bulan, terhitung sejak tanggal ………………………. dengan periode jatuh tempo sebagai berikut: Rp ……………………. + Rp. ………………………. pada tanggal ………………….. secara utuh tanpa potongan (100%) serta dapat diperpanjang dengan persetujuan kedua belah pihak untuk jangka waktu yang sama.
</p>
<p>2.	Jangka waktu perjanjian berakhir manakala Pihak Pertama menginginkan DANA INVESTASI tersebut diminta kembali untuk keseluruhannya, dengan catatan DANA INVESTASI telah 1.5 (satu setengah) bulan sejak perjanjian ini di tandatangani dan Pihak Pertama memberikan pemberitahuan untuk meminta kembali DANA INVESTASI paling lambat 1 (satu) minggu sebelum diserahkan kembali oleh Pihak Kedua, maka Pihak Kedua akan mengembalikan DANA INVESTASI kepada Pihak Pertama sejumlah modal dengan pembagian hasil sesuai Pasal II Ayat 3.
</p>

<p>PASAL IV</p>
<p>HAK DAN KEWAJIBAN PIHAK PERTAMA</p>

<p>Dalam Perjanjian Kerjasama ini, Pihak Pertama memiliki Hak dan Kewajiban sebagai berikut :</p>
<p>1.	Memberikan DANA INVESTASI kepada Pihak Kedua sebesar Rp. ………………………..,- (terbilang)
</p>
<p>2.	Berhak meminta kembali DANA INVESTASI yang telah diserahkan kepada Pihak Kedua dengan ketentuan berdasarkan Pasal III Ayat 2.
</p>
<p>3.	Menerima hasil keuntungan atas pengelolaan DANA INVESTASI, sesuai dengan Pasal VI perjanjian ini.
</p>
<p>PASAL V</p>
<p>HAK DAN KEWAJIBAN PIHAK KEDUA</p>

<p>Dalam Perjanjian Kerjasama Investasi Modal ini, Pihak Kedua memiliki Hak dan Kewajiban sebagai berikut :
</p>
<p>1.	Menerima DANA INVESTASI dari Pihak Pertama sebesar Rp. ………………….,- (terbilang)
</p>
<p>2.	Memberikan bagian hasil keuntungan kepada Pihak Pertama, sesuai dengan Pasal VI perjanjian ini.
</p>
<p>PASAL VI</p>
<p>PEMBAGIAN HASIL</p>

<p>Dalam Perjanjian Kerjasama Investasi Modal ini, kedua belah pihak sepakat didalam hal pembagian hasil investasi penyertaan dana  sebagai berikut :
</p>
<p>1.	Kedua belah pihak sepakat dan setuju bahwa perjanjian kerjasama ini dilakukan dengan  cara  pemberian keuntungan yang diperoleh dalam Usaha Peningkatan Modal Investasi di…………………………. yang berlokasi di ………………………… sebagaimana Pasal II ayat 3 perjanjian ini.
</p>
<p>2.	Bagi hasil yang dimaksud dalam ayat 1 diatas dilakukan dengan memperhitungkan biaya investasi sebagaimana tersebut dalam pasal II ayat 1.
</p>
<p>3.	Bagi hasil yang dimaksud dalam ayat 2 di atas berlaku sampai dengan Pihak Pertama menarik kembali DANA INVESTASI yang telah diserahkan sesuai dengan perhitungan Pasal II ayat 3 perjanjian ini.
</p>



<p>PASAL VII</p>
<p>KEADAAN MEMAKSA (FORCE MAJEUR)</p>

<p>1.	Yang termasuk dalam Force Majeur adalah akibat dari kejadian-kejadian diluar kuasa dan kehendak dari kedua belah pihak diantaranya termasuk tidak terbatas bencana alam, banjir, badai, topan, gempa bumi, kebakaran, perang, huru-hara, pemberontakan, demonstrasi, pemogokan, kegagalan investasi.
</p>
<p>2.	Jika dalam pelaksanaan perjanjian ini terhambat ataupun tertunda baik secara keseluruhan ataupun sebagian yang dikarenakan hal-hal tersebut dalam ayat 1  diatas,  maka  Pihak Kedua bersedia mengganti sejumlah Dana Investasi dari Pihak Pertama secara penuh apabila belum ada pembagian hasil keuntungan, atau pengembalian Dana Investasi dikurangi dengan pembagian hasil yang sudah terima oleh Pihak Pertama.
</p>
<p>3.	Pengembalian Dana Investasi sebagaimana tersebut dalam ayat 2, mengenai tata cara pengembaliannya akan diadakan musyawarah terlebih dahulu antara Pihak Pertama dan Pihak Kedua mengenai proses atau jangka waktu pengembaliannya.
</p>
<p>PASAL VIII</p>
<p>WANPRESTASI</p>

<p>1.	Dalam hal salah satu pihak telah melanggar kewajibannya yang tercantum dalam salah satu Pasal perjanjian ini, telah cukup bukti dan tanpa perlu dibuktikan lebih  lanjut, bahwa pihak yang melanggar tersebut telah melakukan tindakan Wanprestasi.
</p>
<p>2.	Pihak yang merasa dirugikan atas tindakan Wanprestasi tersebut dalam ayat 1 diatas, berhak meminta ganti kerugian dari pihak yang melakukan wanprestasi  tersebut  atas sejumlah kerugian yang dideritanya, kecuali dalam hal kerugian tersebut disebabkan  karena  adanya suatu  keadaan memaksa, seperti tercantum dalam Pasal VII.
</p>
<p>PASAL IX</p>
<p>PERSELISIHAN</p>

<p>Bilamana dalam pelaksanaan perjanjian Kerjasama ini terdapat perselisihan antara  kedua belah pihak baik dalam pelaksanaannya ataupun dalam penafsiran salah satu Pasal dalam perjanjian ini, maka kedua belah pihak sepakat  untuk  sedapat mungkin menyelesaikan perselisihan tersebut dengan cara musyawarah. Apabila musyawarah telah dilakukan oleh kedua belah pihak, namun ternyata tidak  berhasil  mencapai suatu kemufakatan maka Para Pihak sepakat bahwa semua sengketa yang timbul  dari  perjanjian ini akan diselesaikan pada Kantor Kepaniteraan Pengadilan Negeri Jakarta Pusat.
</p>


<p>PASAL X</p>
<p>ATURAN PENUTUP
</p>
<p>Hal-hal yang belum diatur atau belum cukup diatur dalam perjanjian ini apabila dikemudian hari dibutuhkan dan dipandang perlu akan ditetapkan tersendiri secara musyawarah dan selanjutnya akan ditetapkan dalam suatu ADDENDUM yang berlaku mengikat bagi kedua belah pihak, yang akan direkatkan dan merupakan bagian yang tidak terpisahkan dari Perjanjian ini.
</p>
